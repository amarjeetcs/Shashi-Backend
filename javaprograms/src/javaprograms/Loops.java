package javaprograms;

public class Loops {
	
	public static void main(String[] args) {
		for( int i=0;i<=10;i++) {
			 System.out.println("itr:"+ i);
			 
		}
		
		int j=1;
		while(j<=5) {
			System.out.println("itr:"+j);
			j++;
		}
		 int z=10;
		do {
			System.out.println("itr:" + z);
			z--;
		}while(z>=0);
	
	}

}
